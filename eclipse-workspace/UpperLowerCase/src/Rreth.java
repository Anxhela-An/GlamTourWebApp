
public class Rreth 
{ double rreze;
  
  Rreth()
  {
	  rreze=1;
  }
  
  public Rreth(double rrezeERe)
  { rreze = rrezeERe;
	  
  }
  public double tregoSiperfaqen()
  {
	  return rreze*rreze* Math.PI;
  }
  public double tregoPerimetrin()
  {
	  return 2*rreze*Math.PI;
  }
  
  public void percaktoRrezen(double rrezeERe)
  {
	  rreze = rrezeERe;
  }

}
